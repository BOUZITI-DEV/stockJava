    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Date date = txtDate.getDate();
        Client client = (Client) listClient.getSelectedItem();
        if (cmds.create(new Commande(code, date, client))) {
            loadCommande();
            JOptionPane.showMessageDialog(this, "Succès !");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void listCommandeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listCommandeMouseClicked
        // TODO add your handling code here:
        code = Integer.parseInt((model.getValueAt(listCommande.getSelectedRow(), 0).toString()));
        txtDate.setDate((Date) model.getValueAt(listCommande.getSelectedRow(), 1));
        listClient.setSelectedItem((Client) model.getValueAt(listCommande.getSelectedRow(), 2));
    }//GEN-LAST:event_listCommandeMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (code != 0) {
            int rep = JOptionPane.showConfirmDialog(this, "Voulez vous supprimer cet élément ?");
            if (rep == 0) {
                if (cmds.delete(cmds.findById(code))) {
                    loadCommande();
                    JOptionPane.showMessageDialog(this, "Succès !");
                }
                else
                    JOptionPane.showMessageDialog(this, "Echèc !");
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Date date = txtDate.getDate();
        Client client = (Client) listClient.getSelectedItem();
        if(cmds.update(new Commande(code, date, client)))
        {
            loadCommande();
            JOptionPane.showMessageDialog(this, "Succès !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


