    public void loadClient() {
        for (Client c : cs.findAll()) {
            listClient.addItem(c);
        }
    }
    
    public void loadCommande() {
        model.setRowCount(0);
        for (Commande c : cmds.findAll()) {
            model.addRow(new Object[]{c.getCode(), c.getDate(), c.getClient()});
        }
    }

