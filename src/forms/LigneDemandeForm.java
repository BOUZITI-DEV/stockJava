    private void listLigneDemandeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listLigneDemandeMouseClicked
        // TODO add your handling code here:
        dmd = (Demande) model.getValueAt(listLigneDemande.getSelectedRow(), 0);
        prod = (Produit) model.getValueAt(listLigneDemande.getSelectedRow(), 1);
        listDemande.setSelectedItem(model.getValueAt(listLigneDemande.getSelectedRow(), 0));
        listProduit.setSelectedItem(model.getValueAt(listLigneDemande.getSelectedRow(), 1));
        txtQuantite.setText(model.getValueAt(listLigneDemande.getSelectedRow(), 2).toString());
        txtPrixVente.setText(model.getValueAt(listLigneDemande.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_listLigneDemandeMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        dmd = (Demande) listDemande.getSelectedItem();
        prod = (Produit) listProduit.getSelectedItem();
        quantite = Integer.parseInt(txtQuantite.getText());
        prixVente = Double.parseDouble(txtPrixVente.getText());
        if (lds.create(new LigneDemande(dmd, prod, quantite, prixVente))) {
            loadLigneDemande();
            JOptionPane.showMessageDialog(this, "Succès !");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        dmd = (Demande) listDemande.getSelectedItem();
        prod = (Produit) listProduit.getSelectedItem();
        quantite = Integer.parseInt(txtQuantite.getText());
        prixVente = Double.parseDouble(txtPrixVente.getText());
        if(lds.update(new LigneDemande(dmd, prod, quantite, prixVente)))
        {
            loadLigneDemande();
            JOptionPane.showMessageDialog(this, "Succès !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (dmd.getCode() != 0 && prod.getId() != 0) {
            int rep = JOptionPane.showConfirmDialog(this, "Voulez vous supprimer cet élément ?");
            if (rep == 0) {
                if (lds.delete(new LigneDemande(dmd, prod, quantite, prixVente))) {
                    loadLigneDemande();
                    JOptionPane.showMessageDialog(this, "Succès !");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


