    public void loadDemande() {
        for (Demande d : dmds.findAll()) {
            listDemande.addItem(d);
            listDemandeAfficher.addItem(d);
        }
    }
    
    public void loadProduit()
    {
        for(Produit p : prods.findAll())
            listProduit.addItem(p);
    }
    
    public void loadLigneDemande() {
        model.setRowCount(0);
        dmd = (Demande) listDemandeAfficher.getSelectedItem();
        for (LigneDemande l : lds.findAll()) {
            if (l.getDemande().getCode() == dmd.getCode()) {
                model.addRow(new Object[]{l.getDemande(), l.getProduit(), l.getQuantite(), l.getPrixVente()});
            }
        }
    }

