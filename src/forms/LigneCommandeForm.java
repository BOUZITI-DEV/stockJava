    private void listLigneCommandeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listLigneCommandeMouseClicked
        // TODO add your handling code here:
        cmd = (Commande) model.getValueAt(listLigneCommande.getSelectedRow(), 0);
        prod = (Produit) model.getValueAt(listLigneCommande.getSelectedRow(), 1);
        listCommande.setSelectedItem(model.getValueAt(listLigneCommande.getSelectedRow(), 0));
        listProduit.setSelectedItem(model.getValueAt(listLigneCommande.getSelectedRow(), 1));
        txtQuantite.setText(model.getValueAt(listLigneCommande.getSelectedRow(), 2).toString());
        txtPrixVente.setText(model.getValueAt(listLigneCommande.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_listLigneCommandeMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        cmd = (Commande) listCommande.getSelectedItem();
        prod = (Produit) listProduit.getSelectedItem();
        quantite = Integer.parseInt(txtQuantite.getText());
        prixVente = Double.parseDouble(txtPrixVente.getText());
        if (lcs.create(new LigneCommande(cmd, prod, quantite, prixVente))) {
            JOptionPane.showMessageDialog(this, "Succ√®s !");
            loadLigneCommande();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if (cmd.getCode() != 0 && prod.getId() != 0) {
            int rep = JOptionPane.showConfirmDialog(this, "Voulez vous supprimer cet √©l√©ment ?");
            if (rep == 0) {
                if (lcs.delete(new LigneCommande(cmd, prod, quantite, prixVente))) {
                    loadLigneCommande();
                    JOptionPane.showMessageDialog(this, "Succ√®s !");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        cmd = (Commande) listCommande.getSelectedItem();
        prod = (Produit) listProduit.getSelectedItem();
        quantite = Integer.parseInt(txtQuantite.getText());
        prixVente = Double.parseDouble(txtPrixVente.getText());
        if(lcs.update(new LigneCommande(cmd, prod, quantite, prixVente)))
        {
            loadLigneCommande();
            JOptionPane.showMessageDialog(this, "Succ√®s !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


