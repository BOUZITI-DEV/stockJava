    public void loadCommande() {
        for (Commande c : cmds.findAll()) {
            listCommande.addItem(c);
            listCommandeAfficher.addItem(c);
        }
    }

    public void loadProduit() {
        for (Produit p : prods.findAll()) {
            listProduit.addItem(p);
        }
    }

    public void loadLigneCommande() {
        model.setRowCount(0);
        cmd = (Commande) listCommandeAfficher.getSelectedItem();
        for (LigneCommande l : lcs.findAll()) {
            if (l.getCommande().getCode() == cmd.getCode()) {
                model.addRow(new Object[]{l.getCommande(), l.getProduit(), l.getQuantite(), l.getPrixVente()});
            }
        }
    }

