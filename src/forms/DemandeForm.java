    public void loadFournisseur() {
        for (Fournisseur f : fs.findAll()) {
            listFournisseur.addItem(f);
        }
    }

    public void loadDemande() {
        model.setRowCount(0);
        for (Demande d : dmds.findAll()) {
            model.addRow(new Object[]{d.getCode(), d.getDate(), d.getFournisseur()});
        }
    }

