    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        date = txtDate.getDate();
        fournisseur = (Fournisseur) listFournisseur.getSelectedItem();
        if(dmds.create(new Demande(date, fournisseur)))
        {
            loadDemande();
            JOptionPane.showMessageDialog(this, "Succès !");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void listDemmandeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listDemmandeMouseClicked
        // TODO add your handling code here:
        code = Integer.parseInt(model.getValueAt(listDemmande.getSelectedRow(), 0).toString());
        txtDate.setDate((Date) model.getValueAt(listDemmande.getSelectedRow(), 1));
        listFournisseur.setSelectedItem(model.getValueAt(listDemmande.getSelectedRow(), 2));
    }//GEN-LAST:event_listDemmandeMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(code != 0)
        {
            int rep = JOptionPane.showConfirmDialog(this, "Voulez vous supprimer cet élément ?");
            if(rep == 0)
            {
                if(dmds.delete(new Demande(code, date, fournisseur)))
                {
                    loadDemande();
                    JOptionPane.showMessageDialog(this, "Succès !");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        date = txtDate.getDate();
        fournisseur = (Fournisseur) listFournisseur.getSelectedItem();
        if(dmds.update(new Demande(code, date, fournisseur)))
        {
            loadDemande();
            JOptionPane.showMessageDialog(this, "Succès !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


